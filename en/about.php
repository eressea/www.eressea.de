      on other formats like PDF.</p>
    <p>Once you are familiar with the rules, and you decide that Eressea might 
      be the right game for you, you'll have to <a href="register.php">register 
      for the game</a>.</p>
    <h3>First turn</h3>
    <p>On the first Sunday afternoon after your registration you will receive
      an e-mail containing your initial report. It will contain the information
      about your starting position and it is the base from which you do your first
      turn. Reports come in two flavors: Normal report (.nr files, or just NR)
      and Computer reports (.cr files, or simply CR). The NR is a verbose plain
      text file, suitable for reading by humans. The CR file contains the same
      information, but it is not meant for printing - it is the data format that
      Magellan reads. Nowadays, most players use the CR in combination with Magellan
      (and some other tools).</p>
    <h3>Giving orders</h3>
    <p>Based on this initial turn, you will give orders to your units, which you
      will have to send to our server. The documentation tells you how to write
      the orders, and once you're done with them, send them to eressea-server@eressea.upb.de.
      Make sure your mail has the subject ERESSEA ORDERS, or it will not be considered.</p>
